class Solution {
	public int solution(int[][] lines) {
		int answer = 0;
		int[] graph = new int[201];

		/****************************************
		 * lines : [[0, 5], [3, 9], [1, 10]]
		 ****************************************/

		for (int i = 0; i < lines.length; i++) {
			for (int j = lines[i][0]; j < lines[i][1]; j++)
				graph[j + 100]++;
		}

		/********************************************************************************
		 * answer : [(0)1, (1)2, (2)2, (3)3, (4)3, (5)2, (6)2, (7)2, (8)2, (9)1, (10)0] 
		 ********************************************************************************/

		for (int i = 0; i < graph.length - 1; i++) {
			if (graph[i] > 1)
				answer++;
		}

		return answer;
	}
} 